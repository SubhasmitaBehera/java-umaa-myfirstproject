package org.example;

public // keyword: access modifier. (private, protected, default )
class // keyword: used to define a class
AppKeywords // identifier: className
{
    public // keyword: access modifier
    static // keyword: without object we want to access the main method
    void // keyword: return type (what value your method returns). void : not return anything
    main(String[] args) // identifier: methodName: main
    // array of arguments. where each argument is of type String
    {
        // a : character
        // 'a'
        // 'c'
        // String: sequence of characters
        // "welcome"
        // String is a class.
        System.out.println("some message here");
    }
}
